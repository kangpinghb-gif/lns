# ── 大运顺排/逆排 ─────────────────────────────────────

# 阳年男、阴年女 → 顺排；阴年男、阳年女 → 逆排
def is_forward_luck(year_stem: str, gender: str) -> bool:
    """判断大运是顺排还是逆排"""
    stem_yang = {"甲": True, "丙": True, "戊": True, "庚": True, "壬": True,
                 "乙": False, "丁": False, "己": False, "辛": False, "癸": False}
    is_yang = stem_yang.get(year_stem, True)
    if (is_yang and gender == "male") or (not is_yang and gender == "female"):
        return True
    return False
